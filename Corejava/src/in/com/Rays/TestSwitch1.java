package in.com.rays;

public class TestSwitch1 {
	public static void main(String[] args) {
		
	
	int i = 1;
	switch (i) {
	case 1:
		System.out.println("case 1");
		
	case 2:
		System.out.println("case 2");
	case 3:
		System.out.println("case 3");
	default :
		System.out.println("default case");
	}

}
}